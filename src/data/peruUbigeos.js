/**
 * Obtiene las provincias de un departamento
 */
export function getProvincias(departamentoCode) {
  return PROVINCIAS[departamentoCode] || []
}

/**
 * Obtiene los distritos de una provincia
 */
export function getDistritos(departamentoCode, provinciaCode) {
  const key = `${departamentoCode}${provinciaCode}`
  return DISTRITOS[key] || []
}

/**
 * Construye el código de ubigeo completo
 */
export function buildUbigeo(departamentoCode, provinciaCode, distritoCode) {
  return `${departamentoCode}${provinciaCode}${distritoCode}`
}

/**
 * Obtiene el nombre completo de una ubicación dado su ubigeo
 */
export function getUbigeoName(ubigeo) {
  if (!ubigeo || ubigeo.length !== 6) return ''

  const deptCode = ubigeo.substring(0, 2)
  const provCode = ubigeo.substring(2, 4)
  const distCode = ubigeo.substring(4, 6)

  const dept = DEPARTAMENTOS.find(d => d.code === deptCode)
  const prov = (PROVINCIAS[deptCode] || []).find(p => p.code === provCode)
  const dist = (DISTRITOS[`${deptCode}${provCode}`] || []).find(d => d.code === distCode)

  const parts = []
  if (dist) parts.push(dist.name)
  if (prov) parts.push(prov.name)
  if (dept) parts.push(dept.name)

  return parts.join(', ')
}

/**
 * Parsea un ubigeo y devuelve sus componentes
 */
export function parseUbigeo(ubigeo) {
  if (!ubigeo || ubigeo.length !== 6) {
    return { departamento: '', provincia: '', distrito: '' }
  }

  return {
    departamento: ubigeo.substring(0, 2),
    provincia: ubigeo.substring(2, 4),
    distrito: ubigeo.substring(4, 6),
  }
}

export default {
  DEPARTAMENTOS,
  PROVINCIAS,
  DISTRITOS,
  getProvincias,
  getDistritos,
  buildUbigeo,
  getUbigeoName,
  parseUbigeo,
}
